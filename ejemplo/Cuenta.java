public class Cuenta{
    
    String noCuenta;
    String saldo;

    public Cuenta(String noCuenta, String saldo){
        this.noCuenta = noCuenta;
        this.saldo = saldo;
    }

    public String getNoCuenta() {
        return noCuenta;
    }

    public String getSaldo() {
        return saldo;
    }

    
}
